grant select, insert, update, delete on public.discussion_presentation_pages to service_role;
grant select, insert, update on public.discussion_presentation_state to service_role;
grant select on public.discussion_public_display to anon, authenticated, service_role;
grant usage, select on sequence public.discussion_presentation_pages_id_seq to service_role;

