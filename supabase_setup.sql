create or replace function public.refresh_discussion_public_display()
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_state public.discussion_presentation_state%rowtype;
  v_status text := 'waiting';
  v_team_id integer := null;
  v_current_page integer := 1;
  v_total_pages integer := 1;
  v_title text := '';
  v_content text := '';
  v_waiting_message text := '발표를 대기 중입니다.';
  v_finale_titles jsonb := '[]'::jsonb;
  v_page record;
begin
  select *
  into v_state
  from public.discussion_presentation_state
  where id = 1;

  if not found then
    v_state.id := 1;
    v_state.mode := 'waiting';
    v_state.current_team_id := null;
    v_state.current_page_no := 1;
    v_state.waiting_message := '발표를 대기 중입니다.';
    v_state.timer_state := 'reset';
    v_state.timer_remain_secs := 300;
    v_state.timer_last_action_at := timezone('utc', now());
  end if;

  v_waiting_message := coalesce(nullif(trim(v_state.waiting_message), ''), '발표를 대기 중입니다.');

  v_current_page := greatest(coalesce(v_state.current_page_no, 1), 1);

  if v_state.mode = 'finale' then
    v_status := 'finale';

    select coalesce(
      jsonb_agg(
        jsonb_build_object('team', team_id, 'title', title)
        order by team_id, page_no
      ),
      '[]'::jsonb
    )
    into v_finale_titles
    from public.discussion_presentation_pages
    where nullif(trim(title), '') is not null;
  elsif v_state.mode = 'show' and v_state.current_team_id is not null then
    v_team_id := v_state.current_team_id;

    select count(*)
    into v_total_pages
    from public.discussion_presentation_pages
    where team_id = v_team_id;

    v_total_pages := greatest(coalesce(v_total_pages, 0), 1);

    if v_total_pages = 1 then
      select page_no, title, content
      into v_page
      from public.discussion_presentation_pages
      where team_id = v_team_id
      order by page_no
      limit 1;

      if found then
        v_status := 'show';
        v_current_page := v_page.page_no;
        v_title := v_page.title;
        v_content := v_page.content;
      else
        v_status := 'nodata';
      end if;
    else
      select page_no, title, content
      into v_page
      from public.discussion_presentation_pages
      where team_id = v_team_id
        and page_no = v_current_page
      limit 1;

      if not found then
        select page_no, title, content
        into v_page
        from public.discussion_presentation_pages
        where team_id = v_team_id
        order by page_no
        limit 1;
      end if;

      if found then
        v_status := 'show';
        v_current_page := v_page.page_no;
        v_title := v_page.title;
        v_content := v_page.content;
      else
        v_status := 'nodata';
        v_current_page := 1;
      end if;
    end if;
  end if;

  insert into public.discussion_public_display (
    id,
    status,
    team_id,
    current_page,
    total_pages,
    title,
    content,
    waiting_message,
    timer_state,
    timer_remain_secs,
    timer_last_action_at,
    finale_titles,
    updated_at
  )
  values (
    1,
    v_status,
    v_team_id,
    v_current_page,
    v_total_pages,
    v_title,
    v_content,
    v_waiting_message,
    coalesce(v_state.timer_state, 'reset'),
    coalesce(v_state.timer_remain_secs, 300),
    coalesce(v_state.timer_last_action_at, timezone('utc', now())),
    v_finale_titles,
    timezone('utc', now())
  )
  on conflict (id) do update
  set status = excluded.status,
      team_id = excluded.team_id,
      current_page = excluded.current_page,
      total_pages = excluded.total_pages,
      title = excluded.title,
      content = excluded.content,
      waiting_message = excluded.waiting_message,
      timer_state = excluded.timer_state,
      timer_remain_secs = excluded.timer_remain_secs,
      timer_last_action_at = excluded.timer_last_action_at,
      finale_titles = excluded.finale_titles,
      updated_at = excluded.updated_at;
end;
$$;

create or replace function public.refresh_discussion_public_display_trigger()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  perform public.refresh_discussion_public_display();
  return null;
end;
$$;

drop trigger if exists refresh_public_display_on_pages on public.discussion_presentation_pages;
create trigger refresh_public_display_on_pages
after insert or update or delete on public.discussion_presentation_pages
for each statement
execute function public.refresh_discussion_public_display_trigger();

drop trigger if exists refresh_public_display_on_state on public.discussion_presentation_state;
create trigger refresh_public_display_on_state
after insert or update or delete on public.discussion_presentation_state
for each statement
execute function public.refresh_discussion_public_display_trigger();

revoke all on public.discussion_presentation_pages from anon, authenticated;
revoke all on public.discussion_presentation_state from anon, authenticated;
revoke all on sequence public.discussion_presentation_pages_id_seq from anon, authenticated;

